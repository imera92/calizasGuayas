    private void filter(String query) {
        TableRowSorter<DefaultTableModel> tr =new TableRowSorter<DefaultTableModel>(dfm);
        tbCliente.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(query));
    }
    
